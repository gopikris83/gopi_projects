# Integer casting

x = int(5)
y = int(4.9)
z = int("100")

print (x)
print (y)
print (z)
print ()

# Float Casting

x = float(8)
y = float(3.5)
z = float("10.9")
c = float("2")

print(x)
print(y)
print(z)
print(c)
print ()

# String Casting

x = str("S1")
y = str(2)
z = str(3.0)

print (x)
print (y)
print (z)

